let arrNumber = [1, 2, 10, 21, 25, 100, 501]
arrNumber.reverse()
console.log(arrNumber); // [501, 100, 25, 21,10,   2,  1]

let arrNumber1 = [1, 2, 10, 21]
let rev = arrNumber1.reverse()
console.log(rev);   // [ 21, 10, 2, 1 ]
console.log(arrNumber1);  // [ 21, 10, 2, 1 ]


let arrStr = ["mango", "Apple", "Banana"]
let reverse = arrStr.reverse()
console.log(reverse);  // [ 'Banana', 'Apple', 'mango' ]
console.log(arrStr.reverse());  // [ 'mango', 'Apple', 'Banana' ]



let arrobj = [{ id: 1, name: "Raj" }, { id: 2, name: 'Pavan' }]
